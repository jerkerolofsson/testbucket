namespace TestBucket.Traits.Core;

/// <summary>
/// Traits that relate to the test code structure
/// </summary>
public class AutomationTraitNames
{
    /// <summary>
    /// Assembly/binary/dll/exe implementing the test
    /// </summary>
    public const string Assembly = nameof(Assembly);

    /// <summary>
    /// Class
    /// </summary>
    public const string ClassName = nameof(ClassName);

    /// <summary>
    /// Module
    /// </summary>
    public const string Module = nameof(Module);
    
    /// <summary>
    /// Method Name
    /// </summary>
    public const string MethodName = nameof(MethodName);
}
